# 횟수를 입력받아서 그 수만큼 Hello 출력하는 프로그램
# 5를 입력하면
# 2번째 Hello
# 3번째 Hello
# 4번째 Hello
# 5번째 Hello
i = 1
n = int (input('횟수를 입력하세요>>'))
while i<=n:
    print(int(i),'번째' 'Hello')
    i+=1
  
# 2
# 1~100 사이에서 7의 배수만 출력하는 프로그램 (while안에서 if를 사용)
b=1
while b<=100:
    if b%7==0:
        print(b)
    b+=1

